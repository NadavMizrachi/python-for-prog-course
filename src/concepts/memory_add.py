x = 10
y = 10

# `is` checks if the var points to same mem location

print(f"x is y? {x is y}")

a = 4
b = 3
b += 1

print(f"a is b? {a is b}")

# `id` prints memory location
print(f"a id = { id(a) }")
print(f"b id = { id(b) }")


